import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const outputPath = path.join(rootDir, 'src', 'data', 'themealdb-cache.js');
const API_BASE = 'https://www.themealdb.com/api/json/v1/1';

async function fetchJson(url) {
  for (let attempt = 0; attempt < 5; attempt += 1) {
    const response = await fetch(url);
    if (response.ok) {
      return response.json();
    }

    if (response.status === 429 && attempt < 4) {
      const waitMs = 1200 * (attempt + 1);
      await new Promise((resolve) => setTimeout(resolve, waitMs));
      continue;
    }

    throw new Error(`Request failed (${response.status}): ${url}`);
  }

  throw new Error(`Request failed after retries: ${url}`);
}

async function mapWithConcurrency(items, limit, mapper) {
  const results = new Array(items.length);
  let nextIndex = 0;

  async function worker() {
    while (nextIndex < items.length) {
      const current = nextIndex;
      nextIndex += 1;
      results[current] = await mapper(items[current], current);
    }
  }

  await Promise.all(Array.from({ length: Math.min(limit, items.length || 1) }, () => worker()));
  return results;
}

function sortMeals(meals) {
  return [...meals].sort((a, b) => {
    const nameA = String(a?.strMeal || '');
    const nameB = String(b?.strMeal || '');
    return nameA.localeCompare(nameB);
  });
}

async function main() {
  console.log('Fetching TheMealDB categories...');
  const categoryPayload = await fetchJson(`${API_BASE}/list.php?c=list`);
  const categories = Array.isArray(categoryPayload.meals)
    ? categoryPayload.meals.map((row) => row.strCategory).filter(Boolean)
    : [];

  const mealsById = new Map();

  for (const category of categories) {
    console.log(`Fetching category: ${category}`);
    const payload = await fetchJson(`${API_BASE}/filter.php?c=${encodeURIComponent(category)}`);
    const categoryMeals = Array.isArray(payload.meals) ? payload.meals : [];
    const uncached = categoryMeals.filter((meal) => !mealsById.has(String(meal.idMeal)));

    const details = await mapWithConcurrency(uncached, 2, async (meal, index) => {
      if (index > 0) {
        await new Promise((resolve) => setTimeout(resolve, 150));
      }
      const detailPayload = await fetchJson(`${API_BASE}/lookup.php?i=${encodeURIComponent(meal.idMeal)}`);
      return detailPayload.meals?.[0] || null;
    });

    for (const detail of details) {
      if (detail?.idMeal) {
        mealsById.set(String(detail.idMeal), detail);
      }
    }
  }

  const meals = sortMeals(Array.from(mealsById.values()));
  const source = `/* eslint-disable */\n` +
    `// Auto-generated by scripts/sync-themealdb-cache.mjs\n` +
    `// Generated at ${new Date().toISOString()}\n\n` +
    `export const themealdbSnapshot = ${JSON.stringify({ generatedAt: new Date().toISOString(), mealCount: meals.length, meals }, null, 2)};\n`;

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, source, 'utf8');

  console.log(`Saved ${meals.length} meals to ${outputPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});